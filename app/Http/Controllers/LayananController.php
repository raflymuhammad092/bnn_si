    }

    /**
     * Store a newly created resource in storage.
     */
    public function store(Request $request)
    {
        $request->validate([
            'layanan_code' => 'required|unique:layanan|max:255',
            'nama_layanan' => 'required|max:255',
        ]);

        Layanan::create([
            'layanan_code' => $request->layanan_code,
            'nama_layanan' => $request->nama_layanan,
        ]);

        return redirect()->route('kategori-layanan.index')->with('success', 'Layanan berhasil ditambahkan!');
    }

